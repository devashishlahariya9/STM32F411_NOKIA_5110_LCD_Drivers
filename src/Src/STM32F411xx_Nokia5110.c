static void reset_lcd(NOKIA* nokia_handler)
{
	GPIO_Pin(nokia_handler->RST_GPIO, nokia_handler->RST, GPIO_STATE_LOW);
	GPIO_Pin(nokia_handler->RST_GPIO, nokia_handler->RST, GPIO_STATE_HIGH);
}

static void lcd_datawrt(NOKIA* nokia_handler, uint8_t _data)
{
	SPI_Write(nokia_handler->SPI, _data);
}

static void lcd_command(NOKIA* nokia_handler, uint8_t _cmd)
{
	GPIO_Pin(nokia_handler->DC_GPIO, nokia_handler->DC, GPIO_STATE_LOW);
	lcd_datawrt(nokia_handler, _cmd);
}

static void lcd_dataWrite(NOKIA* nokia_handler, uint8_t _data)
{
	GPIO_Pin(nokia_handler->DC_GPIO, nokia_handler->DC, GPIO_STATE_HIGH);
	lcd_datawrt(nokia_handler, _data);
}

void NOKIA_SetCursor(NOKIA* nokia_handler, uint8_t _x, uint8_t _y)
{
	lcd_command(nokia_handler, 0x40 | _y);
	lcd_command(nokia_handler, 0x80 | _x);
}

void NOKIA_PrintString(NOKIA* nokia_handler, char *ptr)
{
	for(; *ptr != '\0'; ptr++)
	{
		uint8_t idx = ((int)(*ptr) - 32);
			for(int j=0; j<5; j++)
		{
			lcd_dataWrite(nokia_handler, ASCII[idx][j]);
		}
		lcd_dataWrite(nokia_handler, 0x00);
	}
}

void NOKIA_PrintNumber(NOKIA* nokia_handler, int _num)
{
	char arr[20];
	itoa(_num, arr, 10);
	NOKIA_PrintString(nokia_handler, arr);
}

void NOKIA_PrintFloat(NOKIA* nokia_handler, double _num)
{
	char arr[20];
	sprintf(arr, "%.2f", _num);
	NOKIA_PrintString(nokia_handler, arr);
}

void NOKIA_SetContrast(NOKIA* nokia_handler, uint8_t _contrast)
{
	lcd_command(nokia_handler, 0x21);		//H = 1
	lcd_command(nokia_handler, (0x80 | _contrast) + 10);
	lcd_command(nokia_handler, 0x20);		//H = 0
}

void NOKIA_Clear(NOKIA* nokia_handler)
{
	for(int i=0; i<504; i++)
	{
		lcd_datawrt(nokia_handler, 0x00);			//Clear Screen Buffer
	}
}

void NOKIA_Display(NOKIA* nokia_handler)
{
	SPI_EndTransmission(nokia_handler->SPI, nokia_handler->CE_GPIO, nokia_handler->CE);
}

void lcd_printBitmap(NOKIA* nokia_handler, char* _bitmap, uint8_t _bufferSize)
{
	for(int i=0; i<_bufferSize; i++)
	{
		lcd_dataWrite(nokia_handler, _bitmap[i]);
	}
}

void NOKIA_Init(NOKIA* nokia_handler, SPI_PERIPHERALS_t* SPI, void(*remap_func)(void), GPIO_PERIPHERALS_t* CE_GPIO, uint8_t CE, GPIO_PERIPHERALS_t* DC_GPIO, uint8_t DC, GPIO_PERIPHERALS_t* RST_GPIO, uint8_t RST)
{
	nokia_handler->SPI      = SPI;
	nokia_handler->CE_GPIO  = CE_GPIO;
	nokia_handler->DC_GPIO  = DC_GPIO;
	nokia_handler->RST_GPIO = RST_GPIO;
	nokia_handler->CE       = CE;
	nokia_handler->DC       = DC;
	nokia_handler->RST      = RST;

	GPIO_Pin(nokia_handler->RST_GPIO, nokia_handler->RST, GPIO_STATE_HIGH);
	GPIO_Pin(nokia_handler->DC_GPIO,  nokia_handler->DC,  GPIO_STATE_LOW);
	GPIO_SetMode(nokia_handler->RST_GPIO, nokia_handler->RST, GPIO_LOW_SPEED, GPIO_MODE_PUSH_PULL, GPIO_MODE_GPIO);
	GPIO_SetMode(nokia_handler->DC_GPIO,  nokia_handler->DC,  GPIO_LOW_SPEED, GPIO_MODE_PUSH_PULL, GPIO_MODE_GPIO);

	FPU_Init();

	SPI_Init(nokia_handler->SPI, remap_func, nokia_handler->CE_GPIO, nokia_handler->CE, SPI_MASTER_MODE, SPI_MODE_0, SPI_CLK_DIV_BY_2);

	reset_lcd(nokia_handler);

	SPI_BeginTransmission(nokia_handler->SPI, nokia_handler->CE_GPIO, nokia_handler->CE);
	lcd_command(nokia_handler, 0x21);		//H = 1
	lcd_command(nokia_handler, 0x14);		//LCD Bias Mode = 1:48
	lcd_command(nokia_handler, 0x04);		//Set Temp Coefficient
	lcd_command(nokia_handler, 0x20);		//H = 0
	lcd_command(nokia_handler, 0x0C);		//Normal Mode

	while(!(SPI -> SR & (1 << SPI_TXE)));

	GPIO_Pin(nokia_handler->DC_GPIO, nokia_handler->DC, GPIO_STATE_HIGH);
	NOKIA_Clear(nokia_handler);
}
